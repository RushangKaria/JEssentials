package com.coveringarray.parsers;

import com.coveringarray.CoveringArrayParameters;

/**
* A definite interface for covering array
* Parsers.
*/
abstract class CAParser
{
    /**
    * This method parses the covering array based on its type.
    * @return true if the covering array was successfully parsed, false if the file was corrupt or did not follow the type format.
    */
    protected abstract boolean parse();

    /**
    * This method returns the covering array and all its parameters if the parsing was successful.
    * @return the covering array and Parameters if parsing was successful, else returns null.
    */
    protected abstract CoveringArrayParameters getParameters();

}
